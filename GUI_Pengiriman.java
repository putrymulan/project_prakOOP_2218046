    public void tampil() {
        DefaultTableModel tabelhead = new DefaultTableModel();
        tabelhead.addColumn("Nama Toko");
        tabelhead.addColumn("Nama Hijab");
        tabelhead.addColumn("Merk");
        tabelhead.addColumn("Jml Barang");
        tabelhead.addColumn("Harga");
        tabelhead.addColumn("Alamat");
        tabelhead.addColumn("Diskon");
        tabelhead.addColumn("Total");
        
        try {
            koneksi();
            String sql = "SELECT * FROM tb_pengiriman";
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while (res.next()) {
                tabelhead.addRow(new Object[]{res.getString(2), res.getString(3), res.getString(4), res.getString(5), res.getString(6), res.getString(7),res.getString(8),res.getString(9)});
            }
            tabelPengiriman.setModel(tabelhead);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "BELUM TERKONEKSI");
        }
    }
