    private void btn_HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_HapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dataModel = (DefaultTableModel)
        tabelPengiriman.getModel();
        int rowCount = dataModel.getRowCount();
        while (rowCount > 0){
        dataModel.removeRow(rowCount - 1);
        rowCount = dataModel.getRowCount();
        }
    }//GEN-LAST:event_btn_HapusActionPerformed

    private void btn_SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SimpanActionPerformed
        // TODO add your handling code here:
        JOptionPane.showMessageDialog(null, "Data telah ditambahkan ke tabel");
        DefaultTableModel dataModel = (DefaultTableModel)
        tabelPengiriman.getModel();
        List list = new ArrayList<>();
        PengirimanHijab kirim = new PengirimanHijab();
//        TokoHijabMulan Toko = new TokoHijabMulan();
        kirim.JmlBrng(Integer.parseInt(txtJumlah.getText()));
        //        kirim.Alamat(txtAlamat.getText());
        kirim.setAlamat(txtAlamat.getText());
        kirim.Merk(txtMerk.getText());
        kirim.NamaToko(txtToko.getText());
        kirim.NamaHijab(txtNama.getText());
        kirim.Harga(Integer.parseInt(txtHarga.getText()));
        kirim.TotalPendapatan(Integer.parseInt(txtPendapatan.getText()));
        
        list.add(kirim.NamaToko);
        list.add(kirim.NamaHijab);
        list.add(kirim.Merk);
        list.add(kirim.JmlBrng);
        list.add(kirim.Harga);
        list.add(kirim.getAlamat());
        list.add(kirim.Diskon());
        list.add(kirim.TotalPendapatan);
        
        dataModel.addRow(list.toArray());
        clear();
    }//GEN-LAST:event_btn_SimpanActionPerformed

